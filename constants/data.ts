export function getDailyVerse(date: Date): { verse: typeof DAILY_VERSES[0]; isSeasonal: boolean; seasonalTheme?: string } {
  // Get day of year (1-366)
  const start = new Date(date.getFullYear(), 0, 0);
  const diff = date.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay);
  
  // Check for seasonal verses
  const month = date.getMonth(); // 0-11
  const day = date.getDate();
  
  // Advent: Dec 1-24 (before Christmas)
  if (month === 11 && day >= 1 && day <= 24) {
    const seasonalVerse = { ...SEASONAL_VERSES.advent, id: 'seasonal', topic: 'Daily' as const };
    return { verse: seasonalVerse as typeof DAILY_VERSES[0], isSeasonal: true, seasonalTheme: 'Advent' };
  }
  
  // Christmas: Dec 25 - Jan 1 (roughly)
  if ((month === 11 && day === 25) || (month === 0 && day <= 1)) {
    const seasonalVerse = { ...SEASONAL_VERSES.christmas, id: 'seasonal', topic: 'Daily' as const };
    return { verse: seasonalVerse as typeof DAILY_VERSES[0], isSeasonal: true, seasonalTheme: 'Christmas' };
  }
  
  // New Year: Jan 1
  if (month === 0 && day === 1) {
    const seasonalVerse = { ...SEASONAL_VERSES.newYear, id: 'seasonal', topic: 'Daily' as const };
    return { verse: seasonalVerse as typeof DAILY_VERSES[0], isSeasonal: true, seasonalTheme: 'New Year' };
  }
  
  // Valentine's: Feb 14
  if (month === 1 && day === 14) {
    const seasonalVerse = { ...SEASONAL_VERSES.valentine, id: 'seasonal', topic: 'Daily' as const };
    return { verse: seasonalVerse as typeof DAILY_VERSES[0], isSeasonal: true, seasonalTheme: 'Valentine\'s Day' };
  }
  
  // Easter week (rough approximation - Easter Sunday varies)
  // For simplicity, let's use a range around typical Easter dates
  const easterStart = 80; // Around March 21
  const easterEnd = 100; // Around April 10
  if (dayOfYear >= easterStart && dayOfYear <= easterEnd) {
    // Could add more sophisticated Easter calculation
    // For now, let's add a note that this is Easter week
    const seasonalVerse = { ...SEASONAL_VERSES.easter, id: 'seasonal', topic: 'Daily' as const };
    return { verse: seasonalVerse as typeof DAILY_VERSES[0], isSeasonal: true, seasonalTheme: 'Easter Week' };
  }
  
  // Regular daily verse - deterministic based on day of year
  const index = (dayOfYear - 1) % DAILY_VERSES.length;
  return { verse: DAILY_VERSES[index], isSeasonal: false };
}

export function getVerseForDaysAgo(date: Date, daysAgo: number): typeof DAILY_VERSES[0] | null {
  const targetDate = new Date(date);
  targetDate.setDate(targetDate.getDate() - daysAgo);
  
  const start = new Date(targetDate.getFullYear(), 0, 0);
  const diff = targetDate.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay);
  
  // Skip seasonal checks for navigation
  const index = (dayOfYear - 1) % DAILY_VERSES.length;
  return DAILY_VERSES[index];
}

