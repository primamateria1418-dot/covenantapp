// ─── Helper Functions ───────────────────────────────────────────────────────────────

export function getTodayDevotional(): Devotional {
  const today = new Date();
  const dayOfYear = Math.floor((today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24));
  
  // Check for seasonal devotionals first
  const month = today.getMonth();
  const day = today.getDate();
  
  if (month === 11 && day >= 1 && day <= 24) {
    return SEASONAL_DEVOTIONALS[0]; // Advent
  }
  if ((month === 11 && day >= 25) || (month === 0 && day <= 1)) {
    return SEASONAL_DEVOTIONALS[1]; // Christmas
  }
  
  // Easter approximation (March 21 - April 15)
  if (dayOfYear >= 80 && dayOfYear <= 105) {
    return SEASONAL_DEVOTIONALS[2]; // Easter
  }
  
  // Regular devotional - day of year modulo 30
  const devotionalIndex = (dayOfYear - 1) % DEVOTIONALS.length;
  return DEVOTIONALS[devotionalIndex];
}

export function getDevotionalForDay(day: number): Devotional | null {
  if (day < 1 || day > 30) return null;
  return DEVOTIONALS[day - 1];
}
